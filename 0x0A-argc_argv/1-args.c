#include <stdio.h>
#include <stdlib.h>

/**
 * main - Prints the number of arguments
 * @argc: size of the argv array
 * @argv: array containing the program command line arguments
 *
 * Return: Always 0.
 */

int main(int argc, __attribute__((unused)) char *argv[])
{
	printf("%d\n", argc);

	return (0);
}
